#include "3-calc.h"
/**
 * op_add - sum function
 * @a: int
 * @b: int
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract function
 * @a: int
 * @b: int
 * Return: substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication function
 * @a: int
 * @b: int
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - div function
 * @a: int
 * @b: int
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - mod function
 * @a: int
 * @b: int
 * Return: modulo
 */
int op_mod(int a, int b)
{
	return (a % b);
}
